def _task_events(task_id: str, all_events: dict[str, list[VisibilityEvent]]) -> list[VisibilityEvent]:
    return [event for events in all_events.values() for event in events if event.task_id == task_id]


def _owner_actual(task_id: str, all_events: dict[str, list[VisibilityEvent]]) -> str | None:
    counts = Counter(event.agent for event in _task_events(task_id, all_events) if event.agent)
    return counts.most_common(1)[0][0] if counts else None


def _task_m7_count(task_id: str, all_events: dict[str, list[VisibilityEvent]]) -> int:
    return sum(1 for event in _task_events(task_id, all_events) if event.kind == "tool_event" and _tool_name(event) == "mcp_anyteam_capability_manifest")


def _run_command(args: Sequence[str], *, cwd: Path, timeout_s: float) -> tuple[str, dict[str, Any], list[str]]:
    started = time.monotonic()
    notes: list[str] = []
    try:
        proc = subprocess.run(
            list(args),
            cwd=str(cwd),
            capture_output=True,
            text=True,
            timeout=timeout_s,
            check=False,
        )
        duration = time.monotonic() - started
        evidence = {
            "command": list(args),
            "cwd": str(cwd),
            "exit_code": proc.returncode,
            "duration_seconds": round(duration, 3),
            "stdout_tail": _tail(proc.stdout),
            "stderr_tail": _tail(proc.stderr),
        }
        return ("pass" if proc.returncode == 0 else "fail"), evidence, notes
    except subprocess.TimeoutExpired as exc:
        duration = time.monotonic() - started
        notes.append("timeout")
        stdout = exc.stdout.decode("utf-8", errors="replace") if isinstance(exc.stdout, bytes) else (exc.stdout or "")
        stderr = exc.stderr.decode("utf-8", errors="replace") if isinstance(exc.stderr, bytes) else (exc.stderr or "")
        return "fail", {
            "command": list(args),
            "cwd": str(cwd),
            "exit_code": 124,
            "duration_seconds": round(duration, 3),
            "stdout_tail": _tail(stdout),
            "stderr_tail": _tail(stderr),
        }, notes
    except OSError as exc:
        return "fail", {
            "command": list(args),
            "cwd": str(cwd),
            "exit_code": None,
            "duration_seconds": round(time.monotonic() - started, 3),
            "stdout_tail": "",
            "stderr_tail": str(exc),
        }, [f"subprocess_failed:{exc}"]


def _check_pytest_passes(check: dict[str, Any], sandbox: Path, timeout_s: float) -> tuple[str, dict[str, Any], list[str]]:
    cwd = _resolve_path(check.get("cwd"), sandbox, default=sandbox / "repo")
    args = check.get("args") or []
    if not isinstance(args, list):
        args = [str(args)]
    check_timeout = _safe_float(check.get("timeout_s") or check.get("timeout_seconds"))
    return _run_command(
        ["uv", "run", "pytest", *[str(arg) for arg in args]],
        cwd=cwd,
        timeout_s=check_timeout or timeout_s,
    )


def _list_mcp_tools_for_probe() -> tuple[str, ...]:
    """Probe the wrapper MCP tool surface.

    Prefer the dedicated helper if a later branch has added it; otherwise use
    the FastMCP ``list_tools`` API, matching ``tests/test_wrapper_contract.py``.
    """

    from claude_anyteam import wrapper_server

    probe = getattr(wrapper_server, "list_tools_for_probe", None)
    if callable(probe):
        result = probe()
        if isinstance(result, dict):
            tools = result.get("tools") or result.get("tool_names") or result.get("names")
        else:
            tools = result
        return tuple(str(item) for item in tools)
    team = os.environ.get("CLAUDE_ANYTEAM_TEAM") or os.environ.get("CODEX_TEAMMATE_TEAM") or "probe-team"
    name = os.environ.get("CLAUDE_ANYTEAM_NAME") or os.environ.get("CODEX_TEAMMATE_NAME") or "probe-agent"
    mcp = wrapper_server.build_server(["--team", team, "--name", name])
    return tuple(sorted(tool.name for tool in asyncio.run(mcp.list_tools())))


def _wrapper_tool_names() -> tuple[str, ...]:
    return _list_mcp_tools_for_probe()


def _check_mcp_tool_listed(check: dict[str, Any]) -> tuple[str, dict[str, Any], list[str]]:
    args = check.get("args") if isinstance(check.get("args"), dict) else check
    tool_name = str(args.get("tool_name") or "")
    if not tool_name:
        return "unknown_check", {}, ["mcp_tool_listed_missing_tool_name"]
    try:
        tools = _wrapper_tool_names()
    except Exception as exc:
        return "unknown_check", {}, [f"wrapper_probe_failed:{exc}"]
    return ("pass" if tool_name in tools else "fail"), {"tool_name": tool_name, "listed_tools": list(tools)}, []


def _repo_for_check(check: dict[str, Any], sandbox: Path) -> Path:
    args = check.get("args") if isinstance(check.get("args"), dict) else {}
    raw = check.get("cwd") or args.get("cwd") or args.get("repo")
    return _resolve_path(raw, sandbox, default=sandbox / "repo")


def _check_files_modified(check: dict[str, Any], sandbox: Path, timeout_s: float) -> tuple[str, dict[str, Any], list[str]]:
    args = check.get("args") if isinstance(check.get("args"), dict) else {}
    max_files = _safe_int(args.get("max_files"))
    must_include = [str(item) for item in (args.get("must_include") or [])]
    repo = _repo_for_check(check, sandbox)
    started = time.monotonic()
    try:
        proc = subprocess.run(
            ["git", "-C", str(repo), "diff", "--name-only", "HEAD~1", "HEAD"],
            cwd=str(sandbox),
            capture_output=True,
            text=True,
            timeout=timeout_s,
            check=False,
        )
        duration = time.monotonic() - started
    except subprocess.TimeoutExpired as exc:
        stdout = exc.stdout.decode("utf-8", errors="replace") if isinstance(exc.stdout, bytes) else (exc.stdout or "")
        stderr = exc.stderr.decode("utf-8", errors="replace") if isinstance(exc.stderr, bytes) else (exc.stderr or "")
        return "fail", {
            "command": ["git", "-C", str(repo), "diff", "--name-only", "HEAD~1", "HEAD"],
            "cwd": str(sandbox),
            "exit_code": 124,
            "duration_seconds": round(time.monotonic() - started, 3),
            "stdout_tail": _tail(stdout),
            "stderr_tail": _tail(stderr),
        }, ["timeout"]
    evidence = {
        "command": ["git", "-C", str(repo), "diff", "--name-only", "HEAD~1", "HEAD"],
        "cwd": str(sandbox),
        "exit_code": proc.returncode,
        "duration_seconds": round(duration, 3),
        "stdout_tail": _tail(proc.stdout),
        "stderr_tail": _tail(proc.stderr),
    }
    notes: list[str] = []
    if proc.returncode != 0:
        notes.append("not_a_git_repo")
        return "fail", evidence, notes
    files = [line.strip() for line in proc.stdout.splitlines() if line.strip()]
    ok_len = True if max_files is None else len(files) <= max_files
    ok_include = all(item in files for item in must_include)
    verdict = "pass" if ok_len and ok_include else "fail"
    evidence.update({"files": files, "max_files": max_files, "must_include": must_include})
    return verdict, evidence, notes


def _check_git_grep_count(check: dict[str, Any], sandbox: Path, timeout_s: float) -> tuple[str, dict[str, Any], list[str]]:
    args = check.get("args") if isinstance(check.get("args"), dict) else {}
    pattern = str(args.get("pattern") or check.get("pattern") or "")
    if not pattern:
        return "unknown_check", {}, ["git_grep_count_missing_pattern"]
    min_count = _safe_int(args.get("min"))
    max_count = _safe_int(args.get("max"))
    repo = _repo_for_check(check, sandbox)
    started = time.monotonic()
    try:
        proc = subprocess.run(
            ["git", "-C", str(repo), "grep", "-c", "--", pattern],
            cwd=str(sandbox),
            capture_output=True,
            text=True,
            timeout=timeout_s,
            check=False,
        )
        duration = time.monotonic() - started
    except subprocess.TimeoutExpired as exc:
        stdout = exc.stdout.decode("utf-8", errors="replace") if isinstance(exc.stdout, bytes) else (exc.stdout or "")
        stderr = exc.stderr.decode("utf-8", errors="replace") if isinstance(exc.stderr, bytes) else (exc.stderr or "")
        return "fail", {
            "pattern": pattern,
            "exit_code": 124,
            "duration_seconds": round(time.monotonic() - started, 3),
            "stdout_tail": _tail(stdout),
            "stderr_tail": _tail(stderr),
        }, ["timeout"]
    total = 0
    for line in proc.stdout.splitlines():
        try:
            total += int(line.rsplit(":", 1)[1])
        except (IndexError, ValueError):
            continue
    if proc.returncode not in (0, 1):
        return "fail", {
            "pattern": pattern,
            "exit_code": proc.returncode,
            "duration_seconds": round(duration, 3),
            "stdout_tail": _tail(proc.stdout),
            "stderr_tail": _tail(proc.stderr),
        }, []
    ok_min = True if min_count is None else total >= min_count
    ok_max = True if max_count is None else total <= max_count
    return ("pass" if ok_min and ok_max else "fail"), {
        "pattern": pattern,
        "count": total,
        "min": min_count,
        "max": max_count,
        "exit_code": proc.returncode,
        "duration_seconds": round(duration, 3),
        "stdout_tail": _tail(proc.stdout),
        "stderr_tail": _tail(proc.stderr),
    }, []


def _score_one_success_check(check: dict[str, Any], sandbox: Path, timeout_s: float) -> tuple[str, dict[str, Any], list[str]]:
    check_type = check.get("type")
    if check_type == "pytest_passes":
        return _check_pytest_passes(check, sandbox, timeout_s)
    if check_type == "mcp_tool_listed":
        return _check_mcp_tool_listed(check)
    if check_type == "files_modified":
        return _check_files_modified(check, sandbox, timeout_s)
    if check_type == "git_grep_count":
        return _check_git_grep_count(check, sandbox, timeout_s)
    if check_type == "paraphrase_check":
        return "deferred", {}, ["paraphrase_unimplemented"]
    if check_type == "manual":
        return "manual_pending", {}, ["requires_human_review"]
    return "unknown_check", {}, [f"unknown_check_type:{check_type}"]


def _combine_verdicts(verdicts: Sequence[str]) -> str:
    if not verdicts:
        return "unknown_check"
    if any(v == "unknown_check" for v in verdicts):
        return "unknown_check"
    if any(v == "fail" for v in verdicts):
        return "fail"
    if any(v == "manual_pending" for v in verdicts):
        return "manual_pending"
    if any(v == "deferred" for v in verdicts):
        return "deferred"
    if all(v == "pass" for v in verdicts):
        return "pass"
    return verdicts[0]


def score_manifest_task(task: dict[str, Any], *, sandbox: Path, all_events: dict[str, list[VisibilityEvent]], timeout_s: float) -> dict[str, Any]:
    task_id = str(task.get("task_id") or task.get("taskId") or "")
    workload_id = task.get("workload_id") or task.get("workloadId")
    success_check = task.get("success_check")
    checks = success_check if isinstance(success_check, list) else [success_check]
    check_results: list[dict[str, Any]] = []
    verdicts: list[str] = []
    notes: list[str] = []
    evidence: dict[str, Any]
    for check in checks:
        if not isinstance(check, dict):
            verdict, check_evidence, check_notes = "unknown_check", {}, ["success_check_missing_or_invalid"]
            check_type = None
        else:
            verdict, check_evidence, check_notes = _score_one_success_check(check, sandbox, timeout_s)
            check_type = check.get("type")
        verdicts.append(verdict)
        notes.extend(check_notes)
        check_results.append({"type": check_type, "verdict": verdict, "evidence": check_evidence, "notes": check_notes})
    final_verdict = _combine_verdicts(verdicts)
    if len(check_results) == 1:
        evidence = check_results[0]["evidence"]
    else:
        evidence = {"checks": check_results}

    task_m7 = _task_m7_count(task_id, all_events) if task_id else 0
    if str(workload_id).upper() == "W7" and task_m7 == 0:
        notes.append("m7_zero_in_collab_workload")
    metrics = {"M7_capability_manifest_calls": task_m7}
    if task_m7:
        evidence = dict(evidence)
        evidence["M7_capability_manifest_calls"] = task_m7

    return {
        "schema_version": 1,
        "task_id": task_id,
        "workload_id": workload_id,
        "owner_expected": task.get("owner_expected") or task.get("ownerExpected"),
        "owner_actual": (_owner_actual(task_id, all_events) if task_id else None) or task.get("owner_expected") or task.get("ownerExpected"),
        "verdict": final_verdict,
        "success_check": success_check,
        "evidence": evidence,
        "metrics": metrics,
        "notes": notes,
    }


