def aggregate_scenario(
    *,
    scenario: str,
    run_id: str,
    task_scores: Sequence[dict[str, Any]],
    agent_scores: Sequence[dict[str, Any]],
    per_task_files: Sequence[str],
    per_agent_files: Sequence[str],
    input_warnings: Sequence[str],
) -> dict[str, Any]:
    verdict_counter = Counter(str(score.get("verdict")) for score in task_scores)
    task_verdicts = {key: verdict_counter.get(key, 0) for key in ("pass", "fail", "deferred", "manual_pending", "unknown_check")}
    total_tasks = sum(task_verdicts.values())
    task_pass_rate = round(task_verdicts["pass"] / total_tasks, 3) if total_tasks else None
    ratios: list[float] = []
    backends_below: set[str] = set()
    m6 = 0
    m7 = 0
    for score in agent_scores:
        metrics = score["metrics"]
        m6 += int(metrics.get("M6_visibility_degraded_count") or 0)
        m7 += int(metrics.get("M7_capability_manifest_calls") or 0)
        ratio = metrics["M12_envelope_shape_coverage"].get("coverage_ratio")
        if isinstance(ratio, (int, float)):
            ratios.append(float(ratio))
            if ratio < 0.5 and score.get("backend"):
                backends_below.add(str(score["backend"]))
    average_ratio = round(sum(ratios) / len(ratios), 3) if ratios else None
    return {
        "schema_version": 1,
        "scenario": scenario,
        "run_id": run_id,
        "task_verdicts": task_verdicts,
        "task_pass_rate": task_pass_rate,
        "M6_team_total_visibility_degraded": m6,
        "M7_team_total_capability_manifest_calls": m7,
        "M12_team_average_coverage_ratio": average_ratio,
        "M12_backends_below_threshold": sorted(backends_below),
        "per_task_files": list(per_task_files),
        "per_agent_files": list(per_agent_files),
        "input_warnings": list(input_warnings),
    }


def run_score(
    *,
    team: str | None,
    events_dir: Path | None,
    sandbox: Path,
    workload_manifest: Path,
    scenario: str,
    run_id: str,
    out: Path,
    check_timeout_s: float,
) -> dict[str, Any]:
    if not sandbox.exists() or not sandbox.is_dir():
        raise FileNotFoundError(f"sandbox path does not exist: {sandbox}")
    manifest = load_workload_manifest(workload_manifest)
    all_events, warnings = load_events(team=team, events_dir=events_dir)

    task_scores = [
        score_manifest_task(task, sandbox=sandbox, all_events=all_events, timeout_s=check_timeout_s)
        for task in manifest["tasks"]
        if isinstance(task, dict)
    ]
    if len(task_scores) != len(manifest["tasks"]):
        warnings.append("non_object_task_entries_skipped")

    agent_scores = [
        score_agent(agent, events, scenario=scenario, run_id=run_id)
        for agent, events in sorted(all_events.items())
    ]

    per_task_files: list[str] = []
    for score in task_scores:
        rel = Path("tasks") / f"{score['task_id'] or 'unknown'}.json"
        _write_json(out / rel, score)
        per_task_files.append(rel.as_posix())

    per_agent_files: list[str] = []
    for score in agent_scores:
        rel = Path("agents") / f"{score['agent']}.json"
        _write_json(out / rel, score)
        per_agent_files.append(rel.as_posix())

    scenario_score = aggregate_scenario(
        scenario=scenario,
        run_id=run_id,
        task_scores=task_scores,
        agent_scores=agent_scores,
        per_task_files=per_task_files,
        per_agent_files=per_agent_files,
        input_warnings=warnings,
    )
    _write_json(out / "scenario.json", scenario_score)
    return scenario_score


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Score Phase 3 workload correctness and M12 envelope shape coverage.")
    source = parser.add_mutually_exclusive_group(required=True)
    source.add_argument("--team", help="Team name; resolves ~/.claude/teams/<team>/events/*.jsonl")
    source.add_argument("--events-dir", type=Path, help="Explicit archived events directory")
    parser.add_argument("--sandbox", type=Path, required=True, help="Workload sandbox root")
    parser.add_argument("--workload-manifest", type=Path, required=True, help="JSON workload manifest from run_scenario.py")
    parser.add_argument("--scenario", required=True, help="Scenario id, e.g. S5")
    parser.add_argument("--run-id", required=True, help="Run id/timestamp")
    parser.add_argument("--out", type=Path, required=True, help="Output directory for JSON scorecards")
    parser.add_argument("--check-timeout-s", type=float, default=600.0, help=argparse.SUPPRESS)
    return parser


def main(argv: Sequence[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    try:
        scenario_score = run_score(
            team=args.team,
            events_dir=args.events_dir,
            sandbox=args.sandbox,
            workload_manifest=args.workload_manifest,
            scenario=args.scenario,
            run_id=args.run_id,
            out=args.out,
            check_timeout_s=args.check_timeout_s,
        )
    except ManifestError as exc:
        _warn(str(exc))
        return 3
    except FileNotFoundError as exc:
        _warn(str(exc))
        return 1
    except OSError as exc:
        _warn(f"output write failed: {exc}")
        return 2

    print(
        "score_quality: "
        f"task_verdicts={scenario_score['task_verdicts']} "
        f"M12_avg={scenario_score['M12_team_average_coverage_ratio']}",
        file=sys.stderr,
    )
    return 0


if __name__ == "__main__":  # pragma: no cover
    raise SystemExit(main())
