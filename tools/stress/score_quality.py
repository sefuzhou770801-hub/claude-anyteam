class ManifestError(ValueError):
    """Raised when the workload manifest cannot be consumed."""


# ---------------------------------------------------------------------------
# Small utilities


def _warn(message: str) -> None:
    print(f"score_quality: {message}", file=sys.stderr)


def _read_json(path: Path) -> Any:
    return json.loads(path.read_text(encoding="utf-8"))


def _write_json(path: Path, payload: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, sort_keys=True) + "\n", encoding="utf-8")


def _tail(text: str, *, lines: int = 50, chars: int = 12000) -> str:
    selected = "\n".join(text.splitlines()[-lines:])
    return selected[-chars:]


def _resolve_path(value: str | None, sandbox: Path, *, default: Path) -> Path:
    if not value:
        return default
    rendered = str(value).replace("<sandbox>", str(sandbox))
    path = Path(rendered)
    if not path.is_absolute():
        path = sandbox / path
    return path


def _tool_name(event: VisibilityEvent) -> str | None:
    if event.kind != "tool_event":
        return None
    value = event.payload.get("tool_name") or event.payload.get("raw_backend_type")
    return str(value) if value is not None else None


def _safe_float(value: Any) -> float | None:
    try:
        return float(value)
    except (TypeError, ValueError):
        return None


def _safe_int(value: Any) -> int | None:
    if isinstance(value, bool):
        return None
    try:
        return int(value)
    except (TypeError, ValueError):
        return None


# ---------------------------------------------------------------------------
# Input loading


def _events_dir_for_team(team: str) -> Path:
    return cs_messaging.TEAMS_DIR / team / "events"


def _parse_event_file(path: Path, warnings: list[str]) -> list[VisibilityEvent]:
    events: list[VisibilityEvent] = []
    for lineno, line in enumerate(path.read_text(encoding="utf-8").splitlines(), start=1):
        if not line.strip():
            continue
        try:
            events.append(VisibilityEvent.model_validate_json(line))
        except Exception as exc:
            warnings.append(f"{path.name}:{lineno}: malformed VisibilityEvent skipped: {exc}")
    return events


def _read_events_via_protocol_io(resolved_events_dir: Path, agent: str, team: str | None) -> list[VisibilityEvent]:
    """Read one agent log through ``protocol_io.read_events``.

    Live ``--team`` runs already match the substrate layout.  For archived
    ``--events-dir`` runs, temporarily mount the directory under a throwaway
    ``TEAMS_DIR/<team>/events`` shape so the same reader validates envelopes.
    """

    if team:
        return pio.read_events(team, agent)

    old_root = cs_messaging.TEAMS_DIR
    if resolved_events_dir.name == "events":
        inferred_team = resolved_events_dir.parent.name
        cs_messaging.TEAMS_DIR = resolved_events_dir.parent.parent
        try:
            return pio.read_events(inferred_team, agent)
        finally:
            cs_messaging.TEAMS_DIR = old_root

    with tempfile.TemporaryDirectory(prefix="score-quality-events-") as tmp:
        root = Path(tmp)
        inferred_team = "archived"
        mounted = root / inferred_team / "events"
        mounted.parent.mkdir(parents=True, exist_ok=True)
        try:
            os.symlink(resolved_events_dir, mounted, target_is_directory=True)
        except OSError:
            shutil.copytree(resolved_events_dir, mounted)
        cs_messaging.TEAMS_DIR = root
        try:
            return pio.read_events(inferred_team, agent)
        finally:
            cs_messaging.TEAMS_DIR = old_root


def load_events(*, team: str | None, events_dir: Path | None) -> tuple[dict[str, list[VisibilityEvent]], list[str]]:
    resolved = _events_dir_for_team(team) if team else events_dir
    if resolved is None:  # pragma: no cover - argparse guards this
        raise FileNotFoundError("events source missing")
    if not resolved.exists() or not resolved.is_dir():
        raise FileNotFoundError(f"events dir does not exist: {resolved}")
    paths = sorted(path for path in resolved.glob("*.jsonl") if path.is_file())
    if not paths:
        raise FileNotFoundError(f"events dir contains no *.jsonl files: {resolved}")

    warnings: list[str] = []
    by_agent: dict[str, list[VisibilityEvent]] = {}
    for path in paths:
        agent = path.stem
        try:
            by_agent[agent] = _read_events_via_protocol_io(resolved, agent, team)
            continue
        except Exception as exc:
            # Keep post-run scoring alive while still trying the canonical
            # helper first, per the brief.
            warnings.append(f"{path.name}: read_events failed; tolerant parse used: {exc}")
        by_agent[agent] = _parse_event_file(path, warnings)
    return by_agent, warnings


def load_workload_manifest(path: Path) -> dict[str, Any]:
    if not path.exists() or not path.is_file():
        raise ManifestError(f"workload manifest does not exist: {path}")
    try:
        manifest = _read_json(path)
    except json.JSONDecodeError as exc:
        raise ManifestError(f"workload manifest is not valid JSON: {exc}") from exc
    if not isinstance(manifest, dict):
        raise ManifestError("workload manifest must be a JSON object")
    if manifest.get("schema_version") != 1:
        raise ManifestError("workload manifest schema_version must be 1")
    tasks = manifest.get("tasks")
    if not isinstance(tasks, list):
        raise ManifestError("workload manifest must contain a tasks list")
    return manifest


