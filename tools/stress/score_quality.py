# Agent / M12 scoring


def _agent_backend(agent: str, events: Sequence[VisibilityEvent]) -> str | None:
    counts = Counter(event.backend for event in events if event.backend)
    return counts.most_common(1)[0][0] if counts else None


def _infer_backend_from_agent_name(agent: str) -> str | None:
    if agent.startswith("codex-"):
        return "codex_app_server"
    if agent.startswith("gemini-"):
        return "gemini_headless"
    if agent.startswith("kimi-"):
        return "kimi_headless"
    if agent.startswith("claude-"):
        return "claude_native"
    return None


def _partial_events_only(events: Sequence[VisibilityEvent]) -> bool:
    return any(event.kind in TERMINAL_KINDS and event.payload.get("partial_events_available") is False for event in events)


def _flatten_violations(events: Sequence[VisibilityEvent], backend: str | None) -> list[dict[str, Any]]:
    violations: list[dict[str, Any]] = []
    if backend is None:
        return violations
    for event in events:
        if event.kind != "tool_event":
            continue
        payload = event.payload or {}
        category = payload.get("category")
        tool_name = payload.get("tool_name")
        raw_tool_name = str(tool_name) if tool_name is not None else None
        # Gemini ACP legitimately has native events named "tool_call".  For
        # Codex-native host tools, generic names indicate a §1 flattener leak.
        if backend in {"codex_app_server", "codex_exec"} and category == "host_tool":
            whitelist = RAW_TOOL_NAME_WHITELIST.get(backend, set())
            if raw_tool_name not in whitelist:
                violations.append(
                    {
                        "event_id": event.event_id,
                        "raw_tool_name": raw_tool_name,
                        "backend": backend,
                        "kind": event.kind,
                    }
                )
    return violations


def score_agent(agent: str, events: Sequence[VisibilityEvent], *, scenario: str, run_id: str) -> dict[str, Any]:
    backend = _agent_backend(agent, events) or _infer_backend_from_agent_name(agent)
    observed = sorted({event.kind for event in events})
    notes: list[str] = []
    if not events:
        notes.append("empty_log")
    if backend is None:
        notes.append("unknown_backend:empty_log")
        expected: list[str] = []
        coverage_ratio = 0.0
        missing: list[str] = []
    elif backend not in EXPECTED_KINDS_PER_BACKEND:
        notes.append(f"unknown_backend:{backend}")
        expected = []
        coverage_ratio = None
        missing = []
    else:
        expected = list(EXPECTED_KINDS_PER_BACKEND[backend])
        missing = sorted(set(expected) - set(observed))
        partial_only = _partial_events_only(events)
        if partial_only:
            missing = [kind for kind in missing if kind not in {"tool_event", "artifact_event", "turn_progress"}]
        coverage_ratio = (len(expected) - len(missing)) / len(expected) if expected else None
    partial_only = _partial_events_only(events)

    visibility_degraded = [event for event in events if event.kind == "visibility_degraded"]
    surface_counts = {surface: 0 for surface in DEFAULT_SURFACES}
    for event in visibility_degraded:
        surface = str(event.payload.get("surface") or "_unknown_surface")
        surface_counts[surface] = surface_counts.get(surface, 0) + 1
    capability_calls = [event for event in events if event.kind == "tool_event" and _tool_name(event) == "mcp_anyteam_capability_manifest"]
    generic_tool_call_count = sum(1 for event in events if event.kind == "tool_event" and _tool_name(event) == "tool_call")
    rounded_coverage = round(coverage_ratio, 3) if isinstance(coverage_ratio, (int, float)) else coverage_ratio

    return {
        "schema_version": 1,
        "agent": agent,
        "backend": backend,
        "scenario": scenario,
        "run_id": run_id,
        "metrics": {
            "M6_visibility_degraded_count": len(visibility_degraded),
            "M6_by_surface": surface_counts,
            "M7_capability_manifest_calls": len(capability_calls),
            "M12_envelope_shape_coverage": {
                "expected_kinds_per_backend": expected,
                "observed_kinds": observed,
                "missing_kinds": missing,
                "coverage_ratio": rounded_coverage,
                "partial_events_only": partial_only,
            },
            "s1_generic_tool_call_count": generic_tool_call_count,
        },
        "s1_flatten_violations": _flatten_violations(events, backend),
        "notes": notes,
    }


def score_agent_events(agent: str, events: Sequence[VisibilityEvent], *, scenario: str, run_id: str) -> dict[str, Any]:
    """Compatibility wrapper used by the brief's unit tests."""

    return score_agent(agent, events, scenario=scenario, run_id=run_id)


def score_task(
    task: dict[str, Any],
    *,
    sandbox: Path,
    owner_actual: str | None = None,
    task_m7: int = 0,
    pytest_timeout_s: float = 600.0,
) -> dict[str, Any]:
    """Score one manifest task without requiring an event-log fixture.

    The production path is ``score_manifest_task``; this helper keeps the
    workload-check runners directly testable.
    """

    events: dict[str, list[VisibilityEvent]] = {}
    if task.get("task_id") and (owner_actual or task_m7):
        # ``score_manifest_task`` derives owner/M7 from events in production.
        # For unit tests, apply explicit overrides after the check result.
        events = {}
    result = score_manifest_task(task, sandbox=sandbox, all_events=events, timeout_s=pytest_timeout_s)
    if owner_actual is not None:
        result["owner_actual"] = owner_actual
    elif result.get("owner_actual") is None:
        result["owner_actual"] = task.get("owner_expected") or task.get("ownerExpected")
    result.setdefault("metrics", {})["M7_capability_manifest_calls"] = task_m7
    if task_m7:
        result["evidence"] = dict(result.get("evidence") or {})
        result["evidence"]["M7_capability_manifest_calls"] = task_m7
    if str(task.get("workload_id") or task.get("workloadId")).upper() == "W7" and task_m7 == 0:
        result.setdefault("notes", [])
        if "m7_zero_in_collab_workload" not in result["notes"]:
            result["notes"].append("m7_zero_in_collab_workload")
    return result


