"""Phase 3 stress quality + M12 envelope-completeness scorer.

This scorer is post-run and read-only.  It consumes the same visibility event
stream the adapters write (``protocol_io.read_events`` for live teams) plus the
workload manifest generated by ``run_scenario.py``.  Workload correctness is
checked against the scenario sandbox; visibility completeness is scored per
agent without flattening backend-native tool names.
"""

